    <h3 class="h1 centered">FAQ virtuelle Telefonanlage</h3>
    <div class="container">
    	<h4 class="title">Was unterscheidet die virtuelle Telefonanlage von CallOne von anderen Anbietern?</h4>
    	<div class="content">
    		<p>
    			CallOne kombiniert eine flexibel anpassbare Cloud-Telefonanlage mit einer umfangreichen Callcenter-Lösung, Integrationen in andere Systeme und offenen APIs. 
    			Die CallOne Telefonielösungen sind somit frei skalierbar und können alle Anforderungen von der kleinen Officetelefonielösung bis hin zu weit ausgewachsenen und voll integrierten Kundenservice- und Sales-Abteilungen abbilden. 
    			Alle Telefoniedaten sind bei CallOne in detaillierten Statistiken auswertbar, die auch über APIs abgefragt oder in BI-Tools <a href="/cti-schnittstellen" title="Schnittstellen">integriert</a> werden können. Kontaktieren Sie uns einfach mit Ihren individuellen Anforderungen!</p>
    	</div>
    </div>
    <div class="container">
    	<h4 class="title">Was kostet die CallOne Telefonanlage?</h4>
    	<div class="content">
    		<p>
        		Sie können die CallOne Cloud-Telefonanlage bereits ab 5,90 € je Nebenstelle nutzen. 
        		Ihnen stehen Optionen im Self-Service oder mit einem persönlichen Customer Succcess Manager zur Verfügung. 
        		Nähere Informationen finden Sie auf unserer <a href="/preise">Preisseite</a> oder <a href="/testen">kontaktieren</a> Sie uns einfach mit <a href="/testen">Ihren Anforderungen</a>.</p>
    	</div>
    </div>
    <div class="container">
    	<h4 class="title">Erhalte ich auch Rufnummern von CallOne?</h4>
    	<div class="content">
    		<p>
    			Wir schalten Ihnen Rufnummern aus allen Vorwahlbereichen in Deutschland sowie lokale Rufnummern weltweit. 
    			Auch Servicerufnummern wie z.B. 0800-Rufnummern stellen wir Ihnen gerne zur Verfügung. 
    			Ebenso können Sie Ihre bereits vorhandenen Rufnummern jederzeit zu uns weiterleiten oder einfach portieren. 
    			Alle Rufnummern im CallOne Netz laufen über stabile Premium-Routen und bieten auszeichnete Sprachqualität.
    		</p>
    	</div>
    </div>
    <div class="container">
    	<h4 class="title">Wie kann ich über die virtuelle Telefonanlage von CallOne telefonieren?</h4>
    	<div class="content">
    		<p>
    			Bei der Wahl des gewünschten Endgeräts sind Sie bei CallOne komplett flexibel. 
    			Nutzen Sie jedes beliebige Softphone oder Hardware-IP-Telefon und kombinieren Sie Ihre Telefonie für Sales und Kundenservice mit Dialerfunktionen und Click-to-Call. 
    			Oder Sie verzichten komplett auf Hard- oder Software-Telefone und nutzen die bei CallOne bereits integrierte Browsertelefonie (WebRTC).
    		</p>
    	</div>
    </div>
    <div class="container">
    	<h4 class="title">Welche Funktionen unterstützt die CallOne Telefonanlage?</h4>
    	<div class="content">
    		<p>
    			Die Cloud-Telefonanlage von CallOne unterstützt alle bekannten Funktionen und Features einer Telefonanlage, z.B. Besetztlampenfelder, Parallelruf, Ringschaltung, Rufnummernübermittlung, uvm. 
    			Darüber hinaus bietet CallOne weitreichende Callcenter-Funktionen wie Warteschleifenadministration, SLAs, IVR-Menüs und Gesprächsaufzeichnung auch im Zusammenspiel mit der Telefonanlage. 
    			Kontaktieren Sie uns gerne mit Ihren Anforderungen! <a href="/voip-telefonanlagen-funktionen">Hier finden Sie mehr zu unseren Features</a>.
    		</p>
    	</div>
    </div>
    <div class="container">
    	<h4 class="title">Mit welchen anderen CRMs und Tools kann ich CallOne kombinieren?</h4>
    	<div class="content">
    		<p>
    			Mit unseren offenen APIs haben Sie Möglichkeit Telefoniefunktionen in nahezu alle CRM-, Kundenservice- und Salestools zu integrieren. 
    			Beliebte Funktionen sind z.B. Click-to-Dial oder das automatisierte Anlegen von Nebenstellen oder Telefonbucheinträgen. 
    			Mehr Informationen zu unseren APIs finden Sie <a href="/cti-schnittstellen" title="´Apps und Schnittstellen">hier</a>.
    		</p>
    		<p>
				Darüber hinaus bietet CallOne eine Vielzahl fertiger Apps für beliebte Tools wie Pipedrive, Salesforce, Zendesk, uvm. sowie Integrationen mit BI-Tools wie tableau und Geckoboard.
    		</p>
