<div class="wrapper">  
    <div class="content wide centered">
        <div class="grid">
          <div class="col-3">
            <span class="sl sl-call-out sl-size-xl"></span>
            <h3>Weiterleitungen</h3>
            <p>Richten Sie Ihre persönliche Rufnummer (z.B. Ihre Servicerufnummer) mit Hilfe verschiedenster Anrufverteilungsoptionen ein. Leiten Sie Anrufe gleichzeitig oder verzögert auf mehrere Zielrufnummern, machen Sie die Weiterleitung abhängig vom Länderursprung des Anrufers (z.B. Anrufer aus Frankreich weiterleiten an französischsprachige Mitarbeiter) oder verteilen Sie Anrufe zu bestimmten Prozentsätzen auf verschiedene Rufnummern.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-arrow-move-up sl-size-xl"></span>
            <h3>IVR-Auswahlmenüs</h3>
            <p>Mit smarten Ansagen und Auswahlmenüs begeistern Sie Ihre Kunden bereits bevor Sie überhaupt einen Mitarbeiter am Telefon haben. Durch Abfrage erster Informationen des Kunden wie beispielsweise Kundenummer oder Kategorie des Anliegens, entsteht eine Art automatisierter Kundenservice. Der Kunde fühlt sich willkommen und sein Anliegen wird von der ersten Minute an ernst genommen.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-recorder sl-size-xl"></span>
            <h3>Gesprächsaufzeichnung</h3>
            <p>Um den optimalen Servicelevel zu halten halten und regelmäßig auf Fehler überprüfen zu können, können Gespräche jederzeit aufgezeichnet werden. Gespräche können auf Anrufer- und Agentenseite aufgezeichnet werden – sowohl manuell als auch voll automatisch.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-fax sl-size-xl"></span>
            <h3>Fax2Mail & Mail2Fax</h3>
            <p>Lassen Sie sich eingehende Faxe ganz einfach an Ihre E-Mail-Adresse senden oder im systemeigenen Cloud-Speicher hinterlegen. Damit sind die Dateien archiviert und jederzeit abrufbar. Sie können auch ganz einfach Faxe von Ihrer E-Mail-Adresse senden und sparen sich somit den Aufwand und die Wartung eines stationären Faxgerätes.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-phone sl-size-xl"></span>
            <h3>Wahlwiederholer</h3>
            <p>Nur eine von vielen Priorisierungsmöglichkeiten für Ihre eingehenden Anrufe. Stellen Sie sicher, dass Anrufer, die bereits zum zweiten oder wiederholten Male anrufen vorranging behandelt werden. So stellen Sie sicher, dass keine Anrufe unter den Tisch fallen und Kunden nicht zu lange auf einen Kontakt warten müssen.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-clock sl-size-xl"></span>
            <h3>Öffnungszeiten administrieren</h3>
            <p>Alle Variationen von Öffnungszeiten sind mit der Lösung abbildbar. Sie haben unterschiedliche Öffnungszeiten an verschiedenen Wochentagen? Kein Problem. Gleiches gilt, sollten sich die Öffnungszeiten der verschieden Skillgruppen unterscheiden.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-bubble-chat sl-size-xl"></span>
            <h3>Mailbox</h3>
            <p>Einfacher war es noch nie! Hinterlegen Sie nach einer Abspannansage oder nach Überschreitung einer bestimmten Wartedauer einfach eine Mailbox. Ihr Kunde kann sein Anliegen aufsprechen und die Datei wird sofort an ein angegebenes E-Mail Postfach geschickt oder im systemeigenen Cloud-Speicher gesichtert.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-call sl-size-xl"></span>
            <h3>Notfallrouting</h3>
            <p>Lassen Sie sich nicht von einem Notfall aus der Bahn werfen. Da Internet fällt aus? Wir leiten weiter auf Festnetz- oder Mobilfunkrufnummern. Stromausfall? Warum nicht alle Anrufer zu einem externen Callcenter weiterleiten oder eine Notfallansage mit Mailbox hinterlegen. Mit uns ist das alles mit wenigen Klicks möglich.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-bubble-smiley sl-size-xl flip flip-before"></span>
            <h3>Kundenbefragung</h3>
            <p>Integrieren Sie eine Kundenbefragung mit beliebig vielen von Ihnen individuell erstellbaren Fragen in Ihre Anrufsteuerung. Ganz einfach am Ende jedes Kundengesprächs oder bei jedem Xten Anruf.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-clock sl-size-xl flip flip-before"></span>
            <h3>Konferenzsystem</h3>
            <p>Starten Sie mit nur einem Klick eine unternehmensinterne Telefonkonferenz oder geben Sie die Konferenz für Kunden oder Lieferanten frei.</p>
          </div>
