            <h3>Individuelle ACD Konfiguration</h3>
            <p>Ihre persönliche ACD wird ganz nach Ihren Wünschen konfiguriert. Sie entscheiden wie viele Agenten und Skillgruppen Sie brauchen, wie die Anrufe auf die jeweiligen Agenten verteilt werden sollen und was passiert, wenn niemand erreichbar ist. Fassen Sie Teams, die an verschiedenen Standorten arbeiten ganz einfach in einer Lösung zusammen. So stellen verschiedene Standorte keine Hürde mehr da und Sie erreichen trotzdem maximale Erreichbarkeit und Servicequalität.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-connection sl-size-xl"></span>
            <h3>Skillgruppen</h3>
            <p>Machen Sie das Meiste aus der Expertise Ihrer Mitarbeiter. Anrufer können bei speziellen Anliegen gezielt auf die Agenten geleitet werden, die am meisten von dem Anliegen verstehen. Priorisieren Sie Ihre Agenten nach Fähigkeiten und teilen Sie sie nach diesen Fähigkeiten in Gruppen auf. So stellen Sie sicher, dass Kunden immer mit dem besten gerade verfügbaren Agenten verbunden werden.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-screen sl-size-xl"></span>
            <h3>Online-Agentenmaske</h3>
            <p>Geben Sie Ihren Agenten eine zentrale Stelle über die sie sich ein- und ausloggen können. So haben Sie immer im Blick wer gerade verfügbar ist. Agenten können ihren Status ändern, wenn sie beispielsweise gerade in der Mittagspause sind und sehen womit ihre Kollegen gerade beschäftigt sind. Außerdem sehen Agenten in Echtzeit alle Infos zu ihrem aktuellen Anruferaufkommen, können ihren Status wechseln oder Anrufe über die Web-Oberfläche annehmen und auslösen.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-profile-serviceman sl-size-xl"></span>
            <h3>Agenten-Administration</h3>
            <p>Versehen Sie jeden Agenten mit individuellen Einstellungen. Verschiedene Priorisierungen, Einteilungen in Skillgruppen, zugewiesene Zielrufnummern, vergebene Zugriffsrechte oder Abrechnungsdaten – alles ist einfach und individuell konfigurierbar und vertrauliche Einstellungen können nicht vom Agenten eingesehen werden. Ab sofort ist es egal zu welchen Zeiten Ihre Agenten arbeiten, denn Sie sehen sofort wer verfügbar ist und die Software berücksichtig dies bei der Anrufverteilung. Agenten sollten eigentlich da sein und gehen nicht ans Telefon oder es ist besetzt? Lassen Sie sich per E-Mail benachrichtigen, wenn so etwas mehr als ein- oder zweimal vorkommt.</p>
