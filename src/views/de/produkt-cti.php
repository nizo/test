<div class="wrapper" id="apis">
    <div class="content wide centered">
    	<h2 class="h1">Telefonie-Integrationen für Ihre eigenen Anwendungen und Apps. Nutzen Sie unsere vielfältigen Schnittstellen</h2>
    </div>
    <div class="content wide centered">
        <div class="grid">   
          <div class="col-3">
            <span class="sl sl-location sl-size-xl"></span>
            <h3>Call Tracking API</h3>
            <ul class="list approved-list">
            	<li>Anrufinformationen aus ACD und VoIP-Telefonanlage in Echtzeit erhalten</li>
				<li>Mit umfassender CTI (Computer Telephony Integration) intelligente Routing-Entscheidungen treffen</li>
				<li>Anrufinformationen an CRM oder Ticketing-Lösung übermitteln</li>            	
            </ul>
          </div>
          <div class="col-3">
            <span class="sl sl-call-out sl-size-xl"></span>
            <h3>Click-to-Call API</h3>
			<ul class="list approved-list">
            	<li>Outbound-Kampagnen über Click-to-Call Funktionen steuern</li>
				<li>Anrufe über API auslösen — egal ob stationäres Telefon oder Softphone-Client</li>
				<li>Einfache Einbindung in Ihre App, CRM oder Sales Tool</li>            	
            </ul>
          </div>
          <div class="col-3">
            <span class="sl sl-pie-chart sl-size-xl"></span>
            <h3>Statistik API</h3>
            <ul class="list approved-list">
            	<li>Alle Statistiken in Echtzeit per API abrufbar und in andere Systeme integrierbar</li>
				<li>Informationen zu aus- und eingehenden Anrufen auf einen Blick</li>
				<li>Statistiken aus allen Bereichen: ACD, VoIP, PSTN, ACD-Agenten</li>            	
            </ul>
          </div>
          <div class="col-3">
            <span class="sl sl-letter sl-size-xl"></span>
            <h3>SMS API</h3>
            <ul class="list approved-list">
            	<li>Jederzeit SMS in Echtzeit an Kunden versenden</li>
				<li>Massenversand direkt über Prämiumrouten der Netzbetreiber</li>
				<li>Hinterlegen von eigenen Absenderkennungen möglich</li>            	
            </ul>
          </div>
          <div class="col-3">
            <span class="sl sl-volume sl-size-xl"></span>
            <h3>Text2Speech API</h3>
			<ul class="list approved-list">
            	<li>Pflegen Sie über die Schnittstelle verschiedenste Texte zur Verwendung im Routing ein</li>
				<li>Texte werden vollautomatisch je nach Routingverlauf angesagt</li>
				<li>Verfügbar in über 30 Sprachen mit einer Vielzahl von SprecherInnen</li>            	
            </ul>
          </div>
          <div class="col-3">
            <span class="sl sl-phone3 sl-size-xl"></span>
            <h3>DTMF API</h3>
            <ul class="list approved-list">
            	<li>Tasteneingaben (DTMF-Töne) empfangen und auf Ihre URL übermitteln</li>
				<li>Ideal zur Abfrage von Kunden- und Rechnungsnummern</li>
				<li>Routingentscheidungen auf Basis der Eingaben treffen</li>            	
            </ul>
          </div>
        </div>
