<div class="wrapper background" id="Sicherheit">
  <div class="content wide">
      <div class="grid">
        <div class="col-2 padding">
          <h2 class="white">Sicherheit</h2>
          <p class="white"></p>
        </div>
        <div class="col-2 padding">
        	<img src="/assets/images/icons_svg/small_15.svg" alt="Funktionen zum Thema Sicherheit" class="white headlineIcon" />
        </div>
      </div>    
  </div>
</div>

<div class="wrapper">  
    <div class="content wide centered">
        <div class="grid">
          <div class="col-3">
            <span class="sl sl-database-network sl-size-xl"></span>
            <h3>Serverstandort Deutschland</h3>
            <p>Die gesamte Technik der CallOne steht in einem Rechenzentrum in Deutschland. Dort sind die gesicherten Server redundant an das Internet, Stromnetze und das deutsche Festnetz angeschlossen. Aufgrund dieser mehrfach redundanten und hochverfügbaren Aufstellung, können wir eine extrem hohe Verfügbarkeit der Systeme gewährleisten.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-lock sl-size-xl"></span>
            <h3>VPN</h3>
            <p>Mit Hilfe unserer Autoprovisionierung können Sie für Ihre Endgeräte einen VPN-Tunnel (Virtual Private Network) aktivieren. Alle Daten werden dann komplett abhörsicher durch diesen Tunnel geleitet, der ein in sich geschlossenes Kommunikationsnetz darstellt.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-lock2 sl-size-xl"></span>
            <h3>TLS</h3>
            <p>Die sogenannte Transport Layer Security (TLS) ist ein Verschlüsselungsprotokoll und sorgt dafür, dass alle Daten eines Telefongespräches (z.B. Rufnummer, Anrufdauer, Anrufernummer) durch eine sichere SSL-Verbindung geleitet werden.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-lock3 sl-size-xl"></span>
            <h3>SRTP</h3>
            <p>Während TLS alle weiteren Daten eines Telefongesprächs verschlüsselt, dreht sich bei SRTP alle um die Sicherheit der Sprachdaten. Alles was während eines Telefonats gesprochen wird, wird damit während der Übertragung vor ungewünschtem Mithören und Abfangen der Daten geschützt.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-call-block sl-size-xl"></span>
            <h3>Black- & Whitelisten</h3>
            <p>Über die Blacklisten-Funktion können Sie Anrufe von unerwünschten Anrufernummern einfach unterdrücken, indem sie die Nummer in einer entsprechenden Liste eintragen. Außerdem können z.B. auch Listen für VIP-Anrufer erstellt werden, die Sie priorisiert durchstellen lassen können.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-ambulance sl-size-xl"></span>
            <h3>Notfall-Management</h3>
            <p>Wir sorgen für den Notfall vor! Richten Sie Ihre Nebenstellen so ein, dass bei einem Internetausfall Anrufe z.B. auf ein Handy geleitet werden oder konfigurieren Sie ein Routing, wenn Endpunkte offline sind. Gerne beraten wir Sie zu einem individuellen Notfallkonzept.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-wallet sl-size-xl"></span>
            <h3>Registrierung beschränkbar</h3>
            <p>Als zusätzliches Sicherheitsfeature können Sie die Registrierung Ihrer Endpunkte auf bestimmte Länder oder IP-Adressen beschränken und damit sicherstellen, dass Ihre Endpunkte nicht von Dritten genutzt werden können.</p>
          </div>
       </div>
    </div>
