<div class="wrapper">  
    <div class="content wide centered">
        <div class="grid">
          <div class="col-3">
            <span class="sl sl-globe sl-size-xl"></span>
            <h3>Standort-Unabhängigkeit</h3>
            <p>Mit CallOne TEL nehmen Sie Ihre Telefonate dort an wo Sie gerade sind: Unterwegs per VoIP-App auf dem Smartphone, im Büro am stationären IP-Telefon oder am Softphone im Home Office — Sie sind flexibel!</p>
          </div>
          <div class="col-3">
            <span class="sl sl-house sl-size-xl"></span>
            <h3>Home Office</h3>
            <p>Nutzen Sie die Vorteile einer virtuellen Telefonanlage auch im HomeOffice. Sie sind unter der gleichen Rufnummer und ohne Weiterleitungen erreichbar wie im Büro, sogar wenn Sie in einem ganz anderen Land sind.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-hand-coin sl-size-xl"></span>
            <h3>Kostenfrei intern telefonieren</h3>
            <p>Alle Nebenstellen der virtuellen Telefonanlage telefonieren über Kurzwahlen untereinander völlig kostenfrei. Dies ist unabhängig davon, ob die Mitarbeiter sich an verschiedenen Standorten oder sogar in verschiedenen Ländern befinden.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-globe2 sl-size-xl"></span>
            <h3>Weltweit einsetzbar</h3>
            <p>CallOne TEL können Sie weltweit einsetzen und damit verschiedenste Standorte und Länder in einer virtuellen Telefonanlage zusammenführen. Ihre Mitarbeiter können im Ausland trotzdem über Ihre deutschen Rufnummern erreichbar sein und intern trotzdem kostenfrei telefonieren.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-cut sl-size-xl"></span>
            <h3>Schnittstellen/APIs</h3>
            <p>Mit unseren vielzähligen Konnektoren zu CRM- und Ticketinglösungen wie Zendesk, Salesforce und Freshdesk sowie weitreichende APIs können Sie Ihre Unternehmenstelefonie mit bereits bestehenden Businesslösungen vollumfänglich integrieren.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-award sl-size-xl"></span>
            <h3>Gesprächsqualität</h3>
            <p>Wir überprüfen zu jeder Zeit die Gesprächs- und Verbindungsqualität der Anrufe über unsere virtuelle Telefonanlage und weisen diese nach jedem Gespräch transparent in Form eines MOS-Werts aus. Die Gesprächsqualität von VoIP-Anschlüssen kann mittlerweile die einer analogen Telefonanlage oder eines klassischen PSTN-Anschlusses weit überschreiten (HD Voice).</p>
          </div>
          <div class="col-3">
            <span class="sl sl-remote sl-size-xl"></span>
            <h3>Zentrale Steuerung über Web-Frontend</h3>
            <p>Die gesamte Einrichtung Ihrer Telefonanlage und alle Nebenstellen können Sie über unsere intuitiv bedienbare Browser-Benutzeroberfläche jederzeit verwalten und ändern. Dort finden Sie auch alle Informationen zu Abrechnungen und Dateien wie Gesprächsaufzeichnungen und Faxe.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-crown sl-size-xl"></span>
            <h3>Premium-Terminierungsrouten</h3>
            <p>Alle Anrufe über unsere IP-Telefonanlage werden über Premium-Routen zugestellt. Auch bei über uns geschalteten eingehenden Rufnummern stellen wir sicher, dass unsere angeschlossenen Carrier immer die optimale Route wählen und damit beste Gesprächsqualität und Aufbauzeiten garantieren können.</p>
          </div>
