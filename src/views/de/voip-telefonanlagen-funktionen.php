<div class="wrapper">  
    <div class="content wide centered">
        <div class="grid">
          <div class="col-3">
            <span class="sl sl-fax sl-size-xl"></span>
            <h3>Stationäre Endgeräte</h3>
            <p>CallOne TEL ist mit stationären IP-Telefonen aller Hersteller kompatibel. Sie können also einfach vorhandene VoIP-Telefone weiter nutzen oder passende neue Telefone anschaffen. Wir können Telefone der Hersteller Snom und Yealink empfehlen, da diese äußerst stabil funktionieren und eine Autprovisionierung mit CallOne TEL möglich ist. Natürlich können Sie auch herkömmliche DECT-Telefone verwenden.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-phone4 sl-size-xl"></span>
            <h3>Softphones</h3>
            <p>Sie möchten keine festen IP-Telefone mehr benutzen? Die Alternative sind sog. Softphones, also Computer-Telefone auf dem PC oder Smartphone. Hier können Sie einfach die Daten Ihres Endpunktes hinterlegen und sind auch unterwegs über die Telefonanlage erreichbar. Diese SIP-Clients gibt es in vielen verschiedenen kostenfreien sowie kostenpflichtigen Varianten.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-mobile-phone sl-size-xl"></span>
            <h3>Autoprovisionierung</h3>
            <p>Sie können Telefone der Hersteller Snom und Yealink sowie die Softphones Bria (kostenpflichtig) und Jitsi (kostenfrei) bequem und automatisiert über die CallOne einrichten. Bei stationären Telefonen müssen Sie dafür nur die MAC-Adresse des IP-Telefons eingeben und ggf. gewünschte Einstellungen direkt in der CallOne Weboberfläche vornehmen. Das Telefon zieht sich alle notwendigen Daten automatisch. Bei Softphones funktioniert die Provisionierung über eine URL, die Sie einmalig in der Oberfläche des Softphones setzen müssen.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-bubble-block2 sl-size-xl"></span>
            <h3>Besetztlampenfeld / Präsenz-Anzeige</h3>
            <p>Über die Besetztlampenfelder stationärer Telefone können Sie jederzeit erkennen, ob Kollegen gerade im Gespräch sind. Außerdem können Sie direkt über die Transfer-Taste und Klick auf ein Besetztlampenfeld Anrufe weiterleiten. Auch einige Softphones bieten diese Funktionenen an.</p>
          </div>
          <div class="col-3">
			<span class="sl sl-bubble-star sl-size-xl"></span>
            <h3>Pick-Up</h3>
            <p>Wenn Sie die Nebenstellenüberwachung mit Snom IP-Telefonen nutzen, können Sie eingehende Anrufe anderer Nebenstellen einfach annehmen, indem Sie das entsprechende Besetztlampenfeld wählen. Dieses blinkt bei eingehenden Anrufen. Die Nebenstellenüberwachung können Sie im Bereich „Endgeräte“ einfach innerhalb der CallOne-Oberfläche einrichten.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-sofa sl-size-xl"></span>
            <h3>Freie Sitzplatzwahl</h3>
            <p>Mit Endgeräten des Herstellers Snom können Sie die freie Sitzplatzwahl ganz einfach in der Autoprovisionierung hinterlegen. Sie können z.B. wählen mit welcher Taste Sie die freie Sitzplatzwahl aktivieren möchten. Ihre Mitarbeiter können sich dann einfach mit einem individuellen PIN am jeweiligen Telefon anmelden und sind nicht mehr auf einen persönlichen Sitzplatz angewiesen.</p>
          </div>
          <div class="col-3">
