          <h2 class="white">Endgeräte</h2>
          <p class="white"></p>
        </div>
        <div class="col-2 padding">
        	<img src="/assets/images/icons_svg/small_22.svg" alt="Funktionen zum Thema Endgeräte" class="white headlineIcon" />
