<div class="wrapper">  
    <div class="content wide centered">
        <div class="grid">
          <div class="col-3">
            <span class="sl sl-bubble sl-size-xl"></span>
            <h3>Ansagen-Management</h3>
            <p>Verwalten und erneuern Sie Ihre Ansagen bequem innerhalb der Browser-Verwaltungsoberfläche der VoIP-Telefonanlage. Neue Ansagen wie Begrüßungsansagen oder Wartemusik können Sie hier bequem per Drag&Drop hinterlegen und im Routing einpflegen.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-bubble-comment sl-size-xl"></span>
            <h3>Text2Speech</h3>
            <p>Sie können Ansagen auch einfach per Text2Speech generieren. Sie tragen den gewünschten Text ein, wählen eine Stimme und Sprache aus und haben binnen weniger Sekunde die passende Ansage, die Sie an allen Stellen im Routing verwenden können.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-contacts sl-size-xl"></span>
            <h3>Benutzermanagement</h3>
            <p>Sie können verschiedene Benutzer mit beschränkten Rechten oder als Admins anlegen. So können Sie sicherstellen, dass nur bestimmte Mitarbeiter Zugriff auf das Routing haben und andere aber z.B. Statistiken einsehen können. Diese Benutzerzugänge sind vollkommen unabhängig von den Endpunkten Ihrer Mitarbeiter.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-phone sl-size-xl"></span>
            <h3>Mitarbeiter-Verwaltung</h3>
            <p>Das Kernstück Ihrer Mitarbeiter ist im Zusammenspiel mit der IP-Telefonanlage der Endpunkt. Die Daten des Endpunktes machen sozusagen die Nebenstelle aus und können in beliebig vielen SIP-Telefonen hinterlegt werden. Des Weiteren können Sie je Mitarbeiter z.B. eine individuelle Rufnummernübermittlung, Kurzwahl, Sicherheitsoptionen, u.v.m. einstellen.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-call sl-size-xl"></span>
            <h3>Rufnummern-Übermittlung</h3>
            <p>Nutzen Sie Rufnummernübermittlung, um z.B. Ihre persönliche Durchwahl oder eine Servicerufnummern bei Anrufen übermitteln. Außerdem können Sie natürlich Funktionen wie CLIP/CLIR verwenden, also Ihre Rufnummern z.B. auch unterdrücken oder eine Rufnummer aus Ihrem Portfolio als angewählte Nummer anzeigen lassen.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-receipt sl-size-xl"></span>
            <h3>Abrechnungen</h3>
            <p>Alle ihre Abrechnungen sind an zentraler Stelle in der CallOne Verwaltungsoberfläche zu finden und werden Ihnen auf Wunsch noch per E-Mail oder postalisch zugestellt. Im Zusammenspiel mit der CallOne Cloud ACD Callcenter-Software, können wir Ihnen auch Agenten-Abrechnungen bereitstellen.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-book sl-size-xl"></span>
            <h3>Handbücher</h3>
            <p>Wenn Sie Fragen haben helfen wir Ihnen natürlich gerne persönlich weiter. Sie können aber auch einen Blick in unsere umfangreichen Handbücher werfen, die wir Ihnen gerne zusenden, oder sich in unseren <a href="#">FAQ</a> informieren.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-receipt2 sl-size-xl"></span>
            <h3>Kostenstellen</h3>
            <p>Machen Sie Ihrer Buchhaltung das Leben einfacherer und hinterlegen Sie Kostenstellen, die direkt in Ihrer Abrechnung ersichtlich sind. Sie können Kostenstellen z.B. je Rufnummer, aber auch für jede Nebenstelle oder Gruppen von Nebenstellen vergeben.</p>
          </div>
