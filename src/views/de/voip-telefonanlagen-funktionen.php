<div class="wrapper">  
    <div class="content wide centered">
        <div class="grid">
          <div class="col-3">
            <span class="sl sl-phone2 sl-size-xl"></span>
            <h3>Rufnummern (0800, Service, lokal)</h3>
            <p>Über CallOne können Sie sämtliche Rufnummerngassen aus mehr als 60 Ländern bestellen. Sei es eine 0800-Servicerufnummer oder lokale Rufnummern aus Deutschland oder anderen Ländern.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-hourglass sl-size-xl"></span>
            <h3>Warteschleife</h3>
            <p>Mit CallOne TEL haben Sie einige Möglichkeiten Warteschleifen, Überläufe und komplexe Routings abzubilden. Nutzen Sie in verschiedenen Modulen Warteschleifenfunktionen oder bilden Sie eine „ACD Light“ über VoIP-Gruppenfunktionalitäten ab.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-call-in sl-size-xl"></span>
            <h3>Rückruf-Option</h3>
            <p>Sie können Ihren Kunden, wenn z.B. gerade kein Mitarbeiter verfügbar ist oder sie außerhalb der Öffnungszeiten anrufen, einfach einen Rückruf anbieten. Der Kunden muss diesen nur durch Drücken einer Taste auf seiner Telefontastatur bestätigen und Sie bekommen automatisch eine E-Mail mit allen nötigen Daten zugesandt.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-connection2 sl-size-xl"></span>
            <h3>Gruppenanruf</h3>
            <p>Nutzen Sie die VoIP-Gruppen-Funktion, um mehrere Nebenstellen/Endpunkte einer Gruppe (z.B. Marketing, Vertrieb, Zentrale) zuordnen zu können. Eine solche Gruppe können Sie dann über verschiedenste Routingmodule ansteuern und die einzelnen Nebenstellen parallel oder als Ringschaltung klingeln lassen.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-call-out sl-size-xl"></span>
            <h3>Rufumleitung</h3>
            <p>Zur Um- bzw. Weiterleitung Ihrer Anrufe haben Sie verschiedenste Möglichkeiten: Hinterlegen Sie auf Ihrem Endgerät Kurzwahlen oder andere Nebenstellen, um Anrufe bequem per Klick weiterleiten zu können. Außerdem können Sie feste Umleitungen im Routing hinterlegen, z.B. eine Weiterleitung auf Ihr Handy, eine Mailbox oder zu Kollegen.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-soda sl-size-xl"></span>
            <h3>Überlaufroutings</h3>
            <p>In unseren verschiedenen Modulen haben Sie an vielen Stellen die Möglichkeit einen Überlauf zu bestimmen. Das bedeutet, dass z.B. ein Anrufer nach einer bestimmten Wartedauer umgeleitet wird, beispielsweise auf eine Mailbox, auf eine andere VoIP-Gruppe oder einen externen Callcenter-Partner.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-phone3 sl-size-xl"></span>
            <h3>IVR-Menüs</h3>
            <p>Mit unserer virtuellen Telefonanlage, können Sie intelligente Module wie z.B. ein IVR-Menü nutzen. In diesem Auswahlmenü können Anrufer per Tastendruck ihr Anliegen auswählen und Sie können das richtige Routing für die verschiedenen Auswahlpunkte hinterlegen.</p>
          </div>
          <div class="col-3">
			<span class="sl sl-scale-all sl-size-xl"></span>    
            <h3>Ursprungsrouting</h3>
            <p>Ein weiteres Modul ist das sog. Ursprungsrouting. Dieses Modul erkennt aus welchem Land oder Vorwahlbereich ein Anrufer kommt und kann diesen dann je nach Ihren Wünschen z.B. direkt Mitarbeitern zuordnen, die die richtige Sprache sprechen.</p>
          </div>
          <div class="col-3">
			<span class="sl sl-bubble-ask sl-size-xl"></span>
            <h3>Kundenbefragung</h3>
            <p>Das beliebte Modul Kundenbefragung gibt Ihnen die Möglichkeit Anrufer nach einem Telefonat zur Qualität des Service oder zum Thema Produktbewertung zu befragen. Die Ergebnisse dieser Umfragen lassen sich danach in den Statistiken spielend einfach auswerten.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-call-add sl-size-xl"></span>
            <h3>Telefonische Rountingänderung</h3>
            <p>Wenn Sie mal keinen Zugriff auf die Weboberfläche der Lösung haben, weil Sie z.B. unterwegs sind, können voreingestellte Optionen zur telefonischen Routingänderung eine gute Lösung sein. Sie können dann über die Anwahl einer Rufnummer beispielsweise eine Servicerufnummer auf eine Mailbox oder Störungsansage legen.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-mailbox sl-size-xl"></span>
            <h3>Telefonische Mailboxabfrage</h3>
            <p>Wie Sie es von Ihrem Mobiltelefon gewohnt sind, können Sie zukünftig auch Sprachnachrichten Ihrer VoIP-Telefonanlage bequem über eine Rufnummer von unterwegs abrufen. Auf diesem Wege können Sie die Mailbox auch an- und abschalten oder die Ansagen neu einsprechen.</p>
          </div>
          <div class="col-3">
            <span class="sl sl-infinity sl-size-xl"></span>
            <h3>Unlimitierte zeitgleiche Anrufe</h3>
            <p>Die Anzahl der möglichen eingehenden und ausgehenden Anrufe über CallOne TEL ist unlimitiert. Bei ausreichender Bandbreite Ihrer Internetleitung sind Ihnen hier keine Grenzen gesetzt. Eine einzelne Nebenstelle kann sogar bis zu 3 ein- und ausgehende Gespräche parallel führen.</p>
          </div>
